// Helper function to get city coordinates
export const getCityCoordinates = (cityName) => {
  const city = pakistanCities.find(
    (c) => c.name.toLowerCase() === cityName.toLowerCase()
  );
  return city ? { lat: city.lat, lng: city.lng } : null;
};

// Helper function to get cities by province
export const getCitiesByProvince = (province) => {
  return pakistanCities.filter((city) => city.province === province);
};

// Get all unique provinces
export const getProvinces = () => {
  return [...new Set(pakistanCities.map((city) => city.province))];
};

// Get all city names as array
export const getCityNames = () => {
  return pakistanCities.map((city) => city.name);
};

